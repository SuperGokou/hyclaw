import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import sharp from "sharp";

// Turns the sticker JPG into a transparent-background mascot PNG by flood-filling
// near-white from the borders (preserves interior whites like the shoe/symbol),
// then emits a base64 TS module the mascot component embeds inline.
const root = path.resolve(import.meta.dirname, "..");
const source = path.join(root, "source", "goku.jpg");
const TARGET_WIDTH = 320;
const WHITE = 236; // r,g,b above this (and connected to a border) become transparent

const { data, info } = await sharp(await readFile(source))
  .resize({ width: TARGET_WIDTH })
  .ensureAlpha()
  .raw()
  .toBuffer({ resolveWithObject: true });

const { width, height } = info;
const isWhite = (i) => data[i] > WHITE && data[i + 1] > WHITE && data[i + 2] > WHITE;
const visited = new Uint8Array(width * height);
const stack = [];
for (let x = 0; x < width; x += 1) {
  stack.push(x, (height - 1) * width + x);
}
for (let y = 0; y < height; y += 1) {
  stack.push(y * width, y * width + (width - 1));
}
while (stack.length) {
  const p = stack.pop();
  if (visited[p]) continue;
  visited[p] = 1;
  const i = p * 4;
  if (!isWhite(i)) continue;
  data[i + 3] = 0; // transparent
  const x = p % width;
  const y = (p - x) / width;
  if (x > 0) stack.push(p - 1);
  if (x < width - 1) stack.push(p + 1);
  if (y > 0) stack.push(p - width);
  if (y < height - 1) stack.push(p + width);
}

const png = await sharp(Buffer.from(data), { raw: { width, height, channels: 4 } })
  .trim()
  .png({ compressionLevel: 9 })
  .toBuffer();

const dataUri = `data:image/png;base64,${png.toString("base64")}`;
const outDir = path.resolve(root, "..", "ui", "src", "components");
await writeFile(
  path.join(outDir, "goku-mascot-data.ts"),
  `// AUTO-GENERATED by branding/scripts/make-goku-mascot.mjs — do not edit by hand.\n` +
    `export const GOKU_MASCOT_PNG = "${dataUri}";\n`,
);
console.log(`goku mascot: ${width}x${height} source, ${(png.length / 1024).toFixed(0)}KB png`);
